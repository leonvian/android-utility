package br.com.lvc.utility.screen.component;

import java.util.Calendar;

import android.app.DatePickerDialog;
import android.content.Context;

public class DatePickerCustomize extends DatePickerDialog {

	
	public DatePickerCustomize(Context context,OnDateSetListener callBack, Calendar calendar) {
		super(context, callBack, calendar.get(Calendar.YEAR), calendar.get(Calendar.MONTH), calendar.get(Calendar.DAY_OF_MONTH));
	}
	
	public DatePickerCustomize(Context context, OnDateSetListener callBack,
			int year, int monthOfYear, int dayOfMonth) {
		super(context, callBack, year, monthOfYear, dayOfMonth);
	}
	
	

}
